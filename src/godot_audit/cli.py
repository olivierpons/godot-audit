def _run_audit(
    args: argparse.Namespace,
    out: OutputHandler,
) -> int:
    """Execute the audit workflow and render the results.

    Args:
        args: Parsed CLI arguments.
        out: Configured output handler.

    Returns:
        Process exit code.
    """
    extra_ignored: list[str] = list(args.ignore_dir or [])
    layout: Layout = Layout(args.layout)

    try:
        auditor: ProjectAuditor = ProjectAuditor(
            args.path,
            layout=layout,
            similarity_threshold=args.threshold,
            extra_ignored_dirs=extra_ignored,
            allow_dashes=args.allow_dashes,
        )
    except (FileNotFoundError, ValueError) as exc:
        out.error(str(exc))
        return EXIT_INVALID_INPUT

    out.verbose(f"Auditing {auditor.project_root}")
    out.verbose(
        f"Layout={layout.value}, threshold={args.threshold}, "
        f"format={args.format}, min_severity={args.severity}"
    )

    full_report: AuditReport = auditor.run()
    filtered: AuditReport = _filter_report(full_report, args.category, args.severity)

    _dispatch_render(
        filtered,
        out=out,
        fmt=args.format,
        output_path=args.output,
        summary_position=_resolve_summary_position(args),
        strip_extension_in_suggested=args.suggested_no_ext,
    )

    return _determine_exit_code(filtered, strict=args.strict)


def _resolve_summary_position(args: argparse.Namespace) -> SummaryPosition:
    """Derive the effective summary position from parsed arguments.

    ``--quiet`` is kept as a convenience alias for
    ``--summary-position none``. When both are passed, ``--quiet`` wins
    because it is the stronger intent (the user explicitly asked for
    no summary).

    Args:
        args: Parsed CLI arguments.

    Returns:
        Resolved summary position.
    """
    if args.quiet:
        return SummaryPosition.NONE
    return SummaryPosition(args.summary_position)


def _dispatch_render(
    report: AuditReport,
    *,
    out: OutputHandler,
    fmt: str,
    output_path: Path | None,
    summary_position: SummaryPosition,
    strip_extension_in_suggested: bool = False,
) -> None:
    """Render *report* according to the requested format and target.

    Text rendering goes through :class:`OutputHandler` (Rich or fallback).
    JSON and Markdown are written to stdout or to *output_path*.

    Args:
        report: Filtered report to render.
        out: Output handler (used for text and status messages).
        fmt: Output format identifier.
        output_path: Optional destination file (None = stdout).
        summary_position: Where to place the summary panel in text mode.
            Ignored for JSON and Markdown formats (which always include
            the summary data in a fixed location).
        strip_extension_in_suggested: Forwarded to the renderer to
            control whether the NAMING table's ``Suggested`` column
            strips the file extension. See
            :class:`AuditRenderer` for the rationale.
    """
    renderer: AuditRenderer = AuditRenderer(
        out,
        strip_extension_in_suggested=strip_extension_in_suggested,
    )

    match fmt:
        case "text":
            if output_path is None:
                renderer.render_text(report, summary_position=summary_position)
            else:
                # For text into a file, write the Markdown view instead:
                # Rich escape sequences would pollute a text file, and
                # JSON is available as a dedicated format anyway.
                output_path.write_text(
                    renderer.render_markdown(report), encoding="utf-8"
                )
                out.success(
                    f"Wrote Markdown report to {output_path} "
                    f"(text into file uses the Markdown view)"
                )
        case "json":
            payload: str = AuditRenderer.render_json(report) + "\n"
            if output_path is None:
                sys.stdout.write(payload)
            else:
                output_path.write_text(payload, encoding="utf-8")
                out.success(f"Wrote JSON report to {output_path}")
        case "markdown":
            payload = renderer.render_markdown(report)
            if output_path is None:
                sys.stdout.write(payload)
            else:
                output_path.write_text(payload, encoding="utf-8")
                out.success(f"Wrote Markdown report to {output_path}")
        case _:
            raise ValueError(f"Unknown format: {fmt}")


def _determine_exit_code(report: AuditReport, *, strict: bool) -> int:
    """Compute the final exit code from a rendered report.

    Args:
        report: Filtered report that was just rendered.
        strict: When True, any INFO issue also triggers a non-zero exit.

    Returns:
        Process exit code.
    """
    if not report.issues:
        return EXIT_OK
    if strict:
        return EXIT_ISSUES_FOUND
    has_warn_or_error: bool = any(
        issue.severity in (Severity.WARNING, Severity.ERROR) for issue in report.issues
    )
    return EXIT_ISSUES_FOUND if has_warn_or_error else EXIT_OK


def main(argv: list[str] | None = None) -> int:
    """Entry point for the CLI.

    Args:
        argv: Optional argument vector (used mainly in tests).

    Returns:
        Process exit code.
    """
    parser: argparse.ArgumentParser = _build_cli_parser()
    args: argparse.Namespace = parser.parse_args(argv)

    out: OutputHandler = OutputHandler(
        verbosity=args.verbosity,
        use_rich=not args.no_rich,
        no_color=args.no_color,
    )

    # Handle info flags first — they bypass the audit entirely
    if args.list_categories:
        _print_categories(out)
        return EXIT_OK
    if args.list_ignored_dirs:
        _print_ignored_dirs(out)
        return EXIT_OK

    return _run_audit(args, out)


if __name__ == "__main__":
    raise SystemExit(main())
