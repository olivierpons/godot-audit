class Layout(StrEnum):
    """Supported project layout conventions.

    The chosen layout drives the :data:`Category.MIRRORING` check.
    """

    #: Scenes and scripts live in separate top-level directories that
    #: mirror each other: ``scenes/entities/slime.tscn`` plus
    #: ``scripts/entities/slime.gd``. This is Godot's default-documented
    #: convention.
    SPLIT = "split"

    #: A scene and its attached script live in the same directory:
    #: ``features/entities/slime.tscn`` plus ``features/entities/slime.gd``.
    #: Preferred for feature-grouped projects.
    COLOCATED = "colocated"


