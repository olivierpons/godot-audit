@dataclass(frozen=True, slots=True, order=True)
class Issue:
    """Single audit finding.

    Attributes:
        severity: Importance level.
        category: Category the issue belongs to.
        path: Project-relative path (forward slashes).
        message: Human-readable explanation, kept for JSON output and
            for callers that want a ready-made summary line.
        suggested: Category-specific suggestion (snake_case stem for
            :data:`Category.NAMING`, destination path for
            :data:`Category.MIRRORING`). ``None`` when not applicable.
        paired_with: Other path involved in the finding, used by
            :data:`Category.NEAR_DUPLICATE` to name the near-identical
            sibling. ``None`` when not applicable.
        detail: Short category-specific marker — the stale suffix for
            :data:`Category.STALE_NAME`, the missing source filename for
            :data:`Category.ORPHAN_COMPANION`. ``None`` when not
            applicable.
    """

    severity: Severity
    category: Category
    path: str
    message: str
    suggested: str | None = None
    paired_with: str | None = None
    detail: str | None = None


@dataclass(slots=True)
class AuditReport:
    """Aggregated results of a full project audit.

    Attributes:
        project_root: Absolute path of the audited project.
        files_scanned: Number of files inspected after ignore rules.
        issues: Every issue found, in discovery order.
    """

    project_root: Path
    files_scanned: int = 0
    issues: IssueList = field(default_factory=list)

    def add(self, issue: Issue) -> None:
        """Append an issue to the report.

        Args:
            issue: Finding to store.
        """
        self.issues.append(issue)

    def group_by_category(self) -> GroupedIssues:
        """Return issues grouped by their category.

        Returns:
            Mapping from category name to issues in that category.
        """
        groups: GroupedIssues = defaultdict(list)
        for issue in self.issues:
            groups[issue.category.value].append(issue)
        return dict(groups)

    def counts_by_severity(self) -> SeverityCounts:
        """Return the number of issues at each severity level.

        Returns:
            Mapping from severity label to count.
        """
        counts: SeverityCounts = defaultdict(int)
        for issue in self.issues:
            counts[issue.severity.value] += 1
        return dict(counts)


