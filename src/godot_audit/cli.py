class AuditRenderer:
    """Render an :class:`AuditReport` via :class:`OutputHandler` or to text.

    Uses Rich tables and panels when available, and falls back to plain
    text otherwise (handled internally by :class:`OutputHandler`).
    """

    def __init__(
        self,
        out: OutputHandler,
        *,
        strip_extension_in_suggested: bool = False,
    ) -> None:
        """Initialize the renderer.

        Args:
            out: Output handler configured by the CLI.
            strip_extension_in_suggested: When ``True``, the NAMING
                table's ``Suggested`` column shows only the stem, with
                the file extension removed. This matches the rename
                workflow in Godot's FileSystem dock and in common
                desktop file managers (GNOME Files / Nautilus, Finder,
                Explorer): pressing F2 or double-clicking a filename
                selects only the stem. A full ``foo.ttf`` suggestion
                pasted over that selection would produce ``foo.ttf.ttf``;
                a bare ``foo`` can be pasted directly.
        """
        self.out: OutputHandler = out
        self.strip_extension_in_suggested: bool = strip_extension_in_suggested

    def _format_extra_value(self, category: Category, raw: str) -> str:
        """Return the extra-column value adjusted for display options.

        Currently only :data:`Category.NAMING` honours
        ``strip_extension_in_suggested`` — other categories (mirroring
        target path, orphan source name, stale suffix, near-duplicate
        sibling path) are displayed as-is.

        Args:
            category: Category of the issue whose value is being
                rendered.
            raw: Raw value as stored on the issue.

        Returns:
            Display-ready value.
        """
        if not raw:
            return ""
        if category is Category.NAMING and self.strip_extension_in_suggested:
            # Drop the last '.ext'; keep inner dots if any.
            dot: int = raw.rfind(".")
            if dot > 0:
                return raw[:dot]
        return raw

    # ── Text rendering ─────────────────────────────────────────

    def render_text(
        self,
        report: AuditReport,
        *,
        summary_position: SummaryPosition = SummaryPosition.TOP,
    ) -> None:
        """Render *report* as text (Rich tables when available).

        Args:
            report: Filtered report to render.
            summary_position: Where to place the summary panel relative
                to the category tables. ``NONE`` suppresses the summary.
        """
        if summary_position is SummaryPosition.TOP:
            self._render_summary(report)

        if not report.issues:
            self.out.success("No issues found.")
            # A BOTTOM summary on an empty report still adds value:
            # the reader sees the "0 files scanned" or threshold info.
            if summary_position is SummaryPosition.BOTTOM:
                self._render_summary(report)
            return

        grouped: GroupedIssues = report.group_by_category()
        for category in CATEGORY_DISPLAY_ORDER:
            issues: IssueList = grouped.get(category.value, [])
            if not issues:
                continue
            self._render_category_table(category, sorted(issues))

        if summary_position is SummaryPosition.BOTTOM:
            self._render_summary(report)

    def _render_summary(self, report: AuditReport) -> None:
        """Render the summary panel.

        Args:
            report: Report whose metadata should be shown.
        """
        counts: SeverityCounts = report.counts_by_severity()
        total: int = len(report.issues)

        lines: list[str] = [
            f"Project: {report.project_root}",
            f"Files scanned: {report.files_scanned}",
            f"Total issues: {total}",
        ]
        for severity in (Severity.ERROR, Severity.WARNING, Severity.INFO):
            count: int = counts.get(severity.value, 0)
            lines.append(f"  {severity.value}: {count}")

        panel = self.out.Panel("\n".join(lines), title="Audit summary")
        self.out.rich_print(panel)

    def _render_category_table(
        self,
        category: Category,
        issues: IssueList,
    ) -> None:
        """Render the issues of a given category as a table.

        The table layout is driven by :data:`_CATEGORY_EXTRA_COLUMN`:
        every table has (Sev, Path); a category-specific third column
        (e.g. ``Suggested`` for naming, ``Near-identical to`` for
        near-duplicates) replaces the previously-shared ``Message``
        column so the same long sentence is never repeated row by row.

        Args:
            category: Category being rendered.
            issues: Sorted issues to display.
        """
        title: str = (
            f"{category.value} ({len(issues)}) — {CATEGORY_DESCRIPTIONS[category]}"
        )
        table = self.out.Table(
            title=title,
            title_style="bold cyan",
            show_lines=False,
        )
        table.add_column("Sev", style="bold", width=6, no_wrap=True)
        table.add_column("Path", style="cyan", overflow="fold")

        extra: tuple[str, str] | None = _CATEGORY_EXTRA_COLUMN.get(category)
        if extra is not None:
            table.add_column(extra[0], overflow="fold")

        for issue in issues:
            sev_style: str = SEVERITY_STYLES[issue.severity]
            if self.out.has_rich:
                sev_cell = self.out.Text(issue.severity.value, style=sev_style)
            else:
                sev_cell = issue.severity.value
            if extra is None:
                table.add_row(sev_cell, issue.path)
            else:
                raw: str = getattr(issue, extra[1]) or ""
                value: str = self._format_extra_value(category, raw)
                table.add_row(sev_cell, issue.path, value)

        self.out.rich_print(table)

    # ── Structured rendering ───────────────────────────────────

    @staticmethod
    def render_json(report: AuditReport) -> str:
        """Return the report as a JSON document.

        Each issue carries its ``severity``, ``category``, ``path`` and
        the full ``message``. Category-specific structured fields
        (``suggested``, ``paired_with``, ``detail``) are emitted only
        when set, so downstream consumers can key on them without
        having to parse the human-readable message.

        Args:
            report: Filtered report to serialize.

        Returns:
            JSON-serialized report.
        """
        issues_payload: list[dict[str, object]] = []
        for issue in report.issues:
            entry: dict[str, object] = {
                "severity": issue.severity.value,
                "category": issue.category.value,
                "path": issue.path,
                "message": issue.message,
            }
            if issue.suggested is not None:
                entry["suggested"] = issue.suggested
            if issue.paired_with is not None:
                entry["paired_with"] = issue.paired_with
            if issue.detail is not None:
                entry["detail"] = issue.detail
            issues_payload.append(entry)

        payload: dict[str, object] = {
            "project_root": str(report.project_root),
            "files_scanned": report.files_scanned,
            "counts_by_severity": report.counts_by_severity(),
            "issues": issues_payload,
        }
        return json.dumps(payload, indent=2, ensure_ascii=False)

    def render_markdown(self, report: AuditReport) -> str:
        """Return the report as a Markdown document.

        The per-category tables honour
        :attr:`strip_extension_in_suggested` just like the text
        renderer, so Markdown copies pasted into a PR match what the
        terminal shows.

        Args:
            report: Filtered report to render.

        Returns:
            Markdown text.
        """
        lines: list[str] = [
            f"# Godot project audit — {report.project_root.name}",
            "",
            f"- **Project root**: `{report.project_root}`",
            f"- **Files scanned**: {report.files_scanned}",
            f"- **Total issues**: {len(report.issues)}",
        ]
        counts: SeverityCounts = report.counts_by_severity()
        for severity in (Severity.ERROR, Severity.WARNING, Severity.INFO):
            lines.append(f"  - **{severity.value}**: {counts.get(severity.value, 0)}")
        lines.append("")

        if not report.issues:
            lines.append("_No issues found._")
            return "\n".join(lines) + "\n"

        grouped: GroupedIssues = report.group_by_category()
        for category in CATEGORY_DISPLAY_ORDER:
            issues: IssueList = grouped.get(category.value, [])
            if not issues:
                continue
            lines.append(
                f"## {category.value} ({len(issues)}) — "
                f"{CATEGORY_DESCRIPTIONS[category]}"
            )
            lines.append("")
            extra: tuple[str, str] | None = _CATEGORY_EXTRA_COLUMN.get(category)
            if extra is None:
                lines.append("| Severity | Path |")
                lines.append("| --- | --- |")
                for issue in sorted(issues):
                    lines.append(f"| {issue.severity.value} | `{issue.path}` |")
            else:
                lines.append(f"| Severity | Path | {extra[0]} |")
                lines.append("| --- | --- | --- |")
                for issue in sorted(issues):
                    raw: str = getattr(issue, extra[1]) or ""
                    value: str = self._format_extra_value(category, raw).replace(
                        "|", r"\|"
                    )
                    lines.append(
                        f"| {issue.severity.value} | `{issue.path}` | {value} |"
                    )
            lines.append("")

        return "\n".join(lines).rstrip() + "\n"


