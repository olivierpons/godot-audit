class ProjectAuditor:
    """Run structural and naming checks against a Godot project root."""

    def __init__(
        self,
        project_root: Path,
        *,
        layout: Layout = Layout.COLOCATED,
        similarity_threshold: float = DEFAULT_SIMILARITY_THRESHOLD,
        extra_ignored_dirs: Iterable[str] = (),
        allow_dashes: bool = True,
    ) -> None:
        """Initialize the auditor.

        Args:
            project_root: Path to the Godot project root (containing
                ``project.godot``).
            layout: Project layout convention. Drives the
                :data:`Category.MIRRORING` check. ``Layout.SPLIT`` expects
                ``scenes/`` and ``scripts/`` to mirror each other;
                ``Layout.COLOCATED`` expects each scene and its attached
                script to live in the same directory.
            similarity_threshold: Cutoff passed to
                :func:`difflib.get_close_matches` for typo detection.
            extra_ignored_dirs: Additional directory names to skip on top
                of :data:`DEFAULT_IGNORED_DIRS`.
            allow_dashes: When ``True`` (default), filename stems may use
                ``-`` as a separator in addition to ``_``; stems like
                ``pixel_operator8-bold`` pass the snake_case check. When
                ``False``, only strict snake_case is accepted.

        Raises:
            FileNotFoundError: If *project_root* does not exist.
            ValueError: If *project_root* is not a Godot project.
        """
        self.project_root: Path = project_root.resolve()
        if not self.project_root.is_dir():
            raise FileNotFoundError(f"Project root does not exist: {self.project_root}")
        if not (self.project_root / "project.godot").is_file():
            raise ValueError(
                f"No project.godot found in {self.project_root}; "
                f"is this really a Godot project root?"
            )
        self.layout: Layout = layout
        self.similarity_threshold: float = similarity_threshold
        self.allow_dashes: bool = allow_dashes
        self.ignored_dirs: frozenset[str] = frozenset(
            DEFAULT_IGNORED_DIRS | set(extra_ignored_dirs)
        )
        self._report: AuditReport = AuditReport(project_root=self.project_root)

    # ── Public entry point ─────────────────────────────────────

    def run(self) -> AuditReport:
        """Execute every check and return the populated report.

        Returns:
            Fully populated audit report.
        """
        all_files: list[Path] = list(self._iter_project_files())
        self._report.files_scanned = len(all_files)

        self._check_mirroring(all_files)
        self._check_asset_naming(all_files)
        self._check_suspicious_suffixes(all_files)
        self._check_duplicates_and_typos(all_files)
        self._check_orphan_companions(all_files)
        self._check_backup_files(all_files)

        return self._report

    # ── Traversal helpers ──────────────────────────────────────

    def _iter_project_files(self) -> Iterator[Path]:
        """Yield every non-ignored file in the project.

        Yields:
            Absolute paths of files to consider.
        """
        for path in self.project_root.rglob("*"):
            if not path.is_file():
                continue
            if self._is_ignored(path):
                continue
            yield path

    def _is_ignored(self, path: Path) -> bool:
        """Return whether a path lives under an ignored directory.

        Args:
            path: Absolute file path.

        Returns:
            True when any path segment matches an ignored directory name.
        """
        try:
            relative: Path = path.relative_to(self.project_root)
        except ValueError:
            return True
        return any(part in self.ignored_dirs for part in relative.parts)

    def _rel(self, path: Path) -> str:
        """Return a path relative to the project root with forward slashes.

        Args:
            path: Absolute path inside the project.

        Returns:
            Forward-slash relative path string.
        """
        return path.relative_to(self.project_root).as_posix()

    # ── Check 1: scene <-> script mirroring ────────────────────

    def _check_mirroring(self, all_files: list[Path]) -> None:
        """Dispatch the mirroring check according to the active layout.

        Args:
            all_files: Every project file to consider.
        """
        match self.layout:
            case Layout.SPLIT:
                self._check_mirroring_split(all_files)
            case Layout.COLOCATED:
                self._check_mirroring_colocated(all_files)

    def _check_mirroring_split(self, all_files: list[Path]) -> None:
        """Flag scripts at ``scripts/`` root whose scene lives in a subdir.

        Expects the project to follow the :data:`Layout.SPLIT` convention
        (``scenes/`` and ``scripts/`` as parallel trees).

        Args:
            all_files: Every project file to consider.
        """
        scripts_root: Path = self.project_root / "scripts"
        scenes_root: Path = self.project_root / "scenes"
        if not scripts_root.is_dir() or not scenes_root.is_dir():
            return

        # Map scene stem -> list of scenes/<subdir> where it lives
        scene_stem_to_subdirs: dict[str, list[str]] = defaultdict(list)
        for file_path in all_files:
            if file_path.suffix not in SCENE_EXTS:
                continue
            try:
                rel_to_scenes: Path = file_path.relative_to(scenes_root)
            except ValueError:
                continue
            parent: str = rel_to_scenes.parent.as_posix()
            if parent in {".", ""}:
                continue
            scene_stem_to_subdirs[file_path.stem].append(parent)

        for file_path in all_files:
            if file_path.suffix not in SCRIPT_EXTS:
                continue
            try:
                rel_to_scripts: Path = file_path.relative_to(scripts_root)
            except ValueError:
                continue
            if rel_to_scripts.parent.as_posix() not in {".", ""}:
                continue

            matching_subdirs: list[str] = scene_stem_to_subdirs.get(file_path.stem, [])
            if not matching_subdirs:
                continue

            suggestion: str = matching_subdirs[0]
            suggested_path: str = f"scripts/{suggestion}/{file_path.name}"
            self._report.add(
                Issue(
                    severity=Severity.WARNING,
                    category=Category.MIRRORING,
                    path=self._rel(file_path),
                    message=(
                        f"Script sits at scripts/ root but its scene is in "
                        f"scenes/{suggestion}/. Suggested move: "
                        f"{suggested_path} "
                        f"(use Godot's FileSystem drag & drop)."
                    ),
                    suggested=suggested_path,
                )
            )

    def _check_mirroring_colocated(self, all_files: list[Path]) -> None:
        """Flag scripts whose matching scene is in a different directory.

        Expects the project to follow the :data:`Layout.COLOCATED`
        convention (scene and attached script in the same directory).

        A script is flagged when a scene with the same stem exists
        elsewhere in the project. The check skips scripts that have no
        matching scene at all (they are standalone, autoloads,
        components, or base classes) and skips scenes under well-known
        script-only directories like ``autoload/`` and ``components/``.

        Args:
            all_files: Every project file to consider.
        """
        # Directories whose contents are expected to be script-only.
        # A script under one of these is never flagged even if a scene
        # with the same stem happens to exist elsewhere.
        script_only_top_dirs: frozenset[str] = frozenset(
            {"autoload", "components", "addons"}
        )

        # Index scenes by stem -> list of directories containing them.
        # A stem may legitimately appear in several dirs; we'll surface
        # all candidates in the suggestion if that happens.
        scene_stem_to_dirs: dict[str, list[Path]] = defaultdict(list)
        for file_path in all_files:
            if file_path.suffix not in SCENE_EXTS:
                continue
            scene_stem_to_dirs[file_path.stem].append(file_path.parent)

        for file_path in all_files:
            if file_path.suffix not in SCRIPT_EXTS:
                continue

            # Skip scripts under script-only top-level directories.
            try:
                relative: Path = file_path.relative_to(self.project_root)
            except ValueError:
                continue
            if relative.parts and relative.parts[0] in script_only_top_dirs:
                continue

            candidate_dirs: list[Path] = scene_stem_to_dirs.get(file_path.stem, [])
            if not candidate_dirs:
                # No scene with this stem -> probably a standalone/base class
                continue
            if file_path.parent in candidate_dirs:
                # Already colocated with at least one matching scene
                continue

            # Prefer the first candidate as the suggested destination
            target_dir: Path = candidate_dirs[0]
            target_rel: str = target_dir.relative_to(self.project_root).as_posix()
            suggested_path: str = f"{target_rel}/{file_path.name}"
            extra_note: str = ""
            if len(candidate_dirs) > 1:
                others: list[str] = [
                    d.relative_to(self.project_root).as_posix()
                    for d in candidate_dirs[1:]
                ]
                extra_note = (
                    f" (other candidates: {', '.join(others)}; "
                    f"pick the one that matches the attached script)"
                )

            self._report.add(
                Issue(
                    severity=Severity.WARNING,
                    category=Category.MIRRORING,
                    path=self._rel(file_path),
                    message=(
                        f"Script is not colocated with its scene. "
                        f"Suggested move: {suggested_path}"
                        f"{extra_note} "
                        f"(use Godot's FileSystem drag & drop)."
                    ),
                    suggested=suggested_path,
                )
            )

    # ── Check 2: snake_case naming ─────────────────────────────

    def _check_asset_naming(self, all_files: list[Path]) -> None:
        """Flag any file whose stem is not snake_case.

        Args:
            all_files: Every project file to consider.
        """
        for file_path in all_files:
            suffix: str = file_path.suffix.lower()
            if suffix in COMPANION_SUFFIXES:
                continue
            if suffix not in SNAKE_CASE_ENFORCED_EXTS:
                continue
            stem: str = file_path.stem
            if stem.endswith(".bak"):
                continue
            pattern: re.Pattern[str] = (
                SNAKE_CASE_WITH_DASH_PATTERN
                if self.allow_dashes
                else SNAKE_CASE_PATTERN
            )
            if pattern.match(stem):
                continue
            suggested_name: str = f"{_to_snake_case(stem)}{file_path.suffix}"
            self._report.add(
                Issue(
                    severity=Severity.INFO,
                    category=Category.NAMING,
                    path=self._rel(file_path),
                    message=(
                        f"Filename stem '{stem}' is not snake_case. "
                        f"Suggested: '{suggested_name}'."
                    ),
                    suggested=suggested_name,
                )
            )

    # ── Check 3: suspicious suffixes ───────────────────────────

    def _check_suspicious_suffixes(self, all_files: list[Path]) -> None:
        """Flag files whose stem ends with a stale-version suffix.

        Args:
            all_files: Every project file to consider.
        """
        for file_path in all_files:
            if file_path.suffix.lower() in COMPANION_SUFFIXES:
                continue
            stem_lower: str = file_path.stem.lower()
            for suffix in SUSPICIOUS_STEM_SUFFIXES:
                if stem_lower.endswith(suffix):
                    self._report.add(
                        Issue(
                            severity=Severity.WARNING,
                            category=Category.STALE_NAME,
                            path=self._rel(file_path),
                            message=(
                                f"Filename ends with '{suffix}' — looks like a "
                                f"stale version. Promote it or delete it."
                            ),
                            detail=suffix,
                        )
                    )
                    break
                    break

    # ── Check 4: near-duplicate detection ──────────────────────

    def _check_duplicates_and_typos(self, all_files: list[Path]) -> None:
        """Flag file pairs in the same directory with similar stems.

        Pairs that differ only by numeric runs (e.g. ``v1``/``v2``,
        ``Bird Call 1``/``Bird Call 3``) are treated as numbered
        variants and skipped. See :func:`_normalize_stem_for_variant_check`.

        Args:
            all_files: Every project file to consider.
        """
        per_dir: dict[tuple[str, str], list[Path]] = defaultdict(list)
        for file_path in all_files:
            if file_path.suffix.lower() in COMPANION_SUFFIXES:
                continue
            key: tuple[str, str] = (
                file_path.parent.as_posix(),
                file_path.suffix.lower(),
            )
            per_dir[key].append(file_path)

        flagged_pairs: set[tuple[str, str]] = set()

        for files in per_dir.values():
            if len(files) < 2:
                continue
            stems: list[str] = [f.stem for f in files]
            for i, file_a in enumerate(files):
                others: list[tuple[int, str]] = [
                    (j, s) for j, s in enumerate(stems) if j != i
                ]
                if not others:
                    continue
                close: list[str] = difflib.get_close_matches(
                    stems[i],
                    [s for _, s in others],
                    n=3,
                    cutoff=self.similarity_threshold,
                )
                for similar_stem in close:
                    # Resolve similar_stem back to the matching file index
                    # via the (index, stem) pairs above. This is robust to
                    # duplicate stems in the same directory (unlike
                    # list.index which always returns the first match).
                    match_index: int | None = next(
                        (j for j, s in others if s == similar_stem),
                        None,
                    )
                    if match_index is None:
                        continue

                    # Skip numbered-variant pairs: filenames that only
                    # differ by their numeric parts (v1/v2, 01/02, …).
                    if _normalize_stem_for_variant_check(
                        stems[i]
                    ) == _normalize_stem_for_variant_check(similar_stem):
                        continue

                    # Last-line filter: if the stems swap one full word
                    # at an aligned position (e.g. guitar/synth), they
                    # are semantically unrelated assets — skip.
                    if _has_totally_different_word(stems[i], similar_stem):
                        continue

                    rel_a: str = self._rel(file_a)
                    rel_b: str = self._rel(files[match_index])
                    pair: tuple[str, str] = tuple(sorted((rel_a, rel_b)))
                    if pair in flagged_pairs:
                        continue
                    flagged_pairs.add(pair)
                    self._report.add(
                        Issue(
                            severity=Severity.WARNING,
                            category=Category.NEAR_DUPLICATE,
                            path=pair[0],
                            message=(
                                f"Near-identical filename to '{pair[1]}' — "
                                f"possible typo or forgotten copy. Confirm "
                                f"which one to keep."
                            ),
                            paired_with=pair[1],
                        )
                    )

    # ── Check 5: orphan .uid / .import ─────────────────────────

    def _check_orphan_companions(self, all_files: list[Path]) -> None:
        """Flag companion files with no matching source.

        Args:
            all_files: Every project file to consider.
        """
        existing_files: set[Path] = set(all_files)
        for file_path in all_files:
            suffix: str = file_path.suffix.lower()
            if suffix not in COMPANION_SUFFIXES:
                continue
            source: Path = file_path.with_suffix("")
            if source in existing_files:
                continue
            self._report.add(
                Issue(
                    severity=Severity.WARNING,
                    category=Category.ORPHAN_COMPANION,
                    path=self._rel(file_path),
                    message=(
                        f"Companion file has no matching source "
                        f"('{source.name}' not found in the same directory). "
                        f"Safe to delete."
                    ),
                    detail=source.name,
                )
            )

    # ── Check 6: backup files ──────────────────────────────────

    def _check_backup_files(self, all_files: list[Path]) -> None:
        """Flag editor backups, ``.orig`` leftovers, and tilde-suffixed files.

        Args:
            all_files: Every project file to consider.
        """
        for file_path in all_files:
            name: str = file_path.name
            if any(pattern.search(name) for pattern in BACKUP_NAME_PATTERNS):
                self._report.add(
                    Issue(
                        severity=Severity.WARNING,
                        category=Category.BACKUP,
                        path=self._rel(file_path),
                        message="Backup-style file (editor leftover). Safe to delete.",
                    )
                )


