"""Godot project structure auditor.

Verify that a Godot project layout matches the conventions defined in
CLAUDE.md section 12 (scenes/ and scripts/ mirroring, components/
separation) and report asset naming issues, orphan companion files,
suspicious duplicates, and backup leftovers.

The tool is read-only: it never modifies the filesystem.

Requires Python 3.14+ and uses :mod:`cli_toolkit` for Rich output and the
split ``-h`` / ``--help`` behaviour.
"""

